    <script src="https://code.jquery.com/jquery-3.7.1.min.js"></script>

    <script>
        $(document).ready(function () {
            $('#email').blur(function () {
                let email = $(this).val();
                let validationError = $('#email-validation-error');

                if (email !== '') {
                    $.ajax({
                        url: '{{ url("/validate-email") }}',
                        type: 'POST',
                        data: {
                            '_token': '{{ csrf_token() }}',
                            'email': email
                        },
                        success: function (response) {
                            if (response === 'exists') {
                                validationError.text('');
                            } else {
                                validationError.text('Not found! Please use your email used while admission, or contact Admin.');
                            }
                        }
                    });
                }
            });
        });
    </script>

